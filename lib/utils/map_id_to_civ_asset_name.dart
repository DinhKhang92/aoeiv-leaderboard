String mapIdToCivAssetName(int id) {
  switch (id) {
    case 0:
      return "abbasid.png";
    case 1:
      return "chinese.png";
    case 2:
      return "delhi.png";
    case 3:
      return "english.png";
    case 4:
      return "french.png";
    case 5:
      return "hre.png";
    case 6:
      return "mongols.png";
    case 7:
      return "rus.png";
    default:
      return "Unknown";
  }
}
